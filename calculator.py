## calculator

x = float(input("First number is: "))
op = input("What is the operation: ")
y = float(input("Second number is: "))


if op == "+":
    print(x+y)
elif op == "-":
    print(x-y)
elif op == "%":
    print(x%y)

elif op == "*":
    print(x*y)

elif op == "/":
    print(x/y)












